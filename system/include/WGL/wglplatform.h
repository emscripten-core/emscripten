/* Macros used in WGL function prototype declarations.
 *
 * WGL functions should be prototyped as:
 *
 * WGLAPI return-type WGLAPIENTRY wglFunction(arguments);
 * typedef return-type (WXPAPIENTRYP PFNWGLFUNCTIONPROC) (arguments);
 *
 * KHRONOS_APICALL and KHRONOS_APIENTRY are defined in KHR/khrplatform.h
 */

#ifndef WGLAPI
#define WGLAPI KHRONOS_APICALL
#endif

#ifndef WGLAPIENTRY
#define WGLAPIENTRY  KHRONOS_APIENTRY
#endif
#define WGLAPIENTRYP WGLAPIENTRY*

/* Define EGLint. This must be a signed integral type large enough to contain
 * all legal attribute names and values passed into and out of EGL, whether
 * their type is boolean, bitmask, enumerant (symbolic constant), integer,
 * handle, or other.  While in general a 32-bit integer will suffice, if
 * handles are 64 bit types, then EGLint should be defined as a signed 64-bit
 * integer type.
 */
typedef khronos_int32_t WGLint;

